    public String register(RegistrationRequest request) {
        boolean isValidEmail = emailValidator.
                test(request.getEmail());
        if (!isValidEmail) {
            return "email not valid";
        }
        if (appUserRepository.existsByUserName(request.getUserName())) {
            return "Username already exists";
        }
        if (appUserRepository.existsByEmail(request.getEmail())) {
            return "email already exists";
        }
        String token = appUserService.signUpUser(
                new AppUser(
                        request.getUserName(),
                        request.getEmail(),
                        request.getPassword(),
                        AppUserRole.USER
                )
        );
//        String newAccessToken = generateAccessToken(request.getUserName());
//
//        // Update the user's account with the new access token
//        appUserService.updateAccessToken(request.getUserName(), newAccessToken);

        String link = "http://localhost:8080/api/v1/registration/confirm?token=" + token;
        emailSender.send(
                request.getEmail(),
                buildEmail(request.getUserName(), link));

        return token;
    }

    @Transactional
    public String confirmToken(String token) {
        ConfirmationTokenEntity confirmationTokenEntity = confirmationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("token not found"));

        if (confirmationTokenEntity.getConfirmedAt() != null) {
            throw new IllegalStateException("email already confirmed");
        }

        LocalDateTime expiredAt = confirmationTokenEntity.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("token expired");
        }

        confirmationTokenService.setConfirmedAt(token);
        appUserService.enableAppUser(
                confirmationTokenEntity.getAppUser().getEmail());
        return "confirmed";
    }

